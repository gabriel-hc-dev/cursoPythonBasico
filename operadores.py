A = int(input("Digite um valor: "))
B = int(input("Digite outro valor: "))
C = int(input("Digite mais um valor: "))

if(A == B):
    print("A e B são iguais.")
else:
    print("A e B são diferentes")

if(A == C):
    print("A e C são iguais.")
else:
    print("A e C são diferentes")

if(B == C):
    print("B e C são iguais.")
else:
    print("B e C são diferentes")
